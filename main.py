def RobotStop():
    PCA9685.set_led_duty_cycle(PCA9685.LEDNum.LED1, 0, 67)
    PCA9685.set_led_duty_cycle(PCA9685.LEDNum.LED2, 0, 67)
    PCA9685.set_led_duty_cycle(PCA9685.LEDNum.LED3, 0, 67)
    PCA9685.set_led_duty_cycle(PCA9685.LEDNum.LED4, 0, 67)
def FrontLedsWhite():
    PCA9685.set_led_duty_cycle(PCA9685.LEDNum.LED5, 0, 67)
    PCA9685.set_led_duty_cycle(PCA9685.LEDNum.LED6, 0, 67)
    PCA9685.set_led_duty_cycle(PCA9685.LEDNum.LED7, 0, 67)
def FrontLedsRed():
    PCA9685.set_led_duty_cycle(PCA9685.LEDNum.LED5, 100, 67)
    PCA9685.set_led_duty_cycle(PCA9685.LEDNum.LED6, 100, 67)
    PCA9685.set_led_duty_cycle(PCA9685.LEDNum.LED7, 0, 67)
# LED1 and LED3 are controlling the direction the rest are controlling the speed
def RobotForward(speed: number):
    PCA9685.set_led_duty_cycle(PCA9685.LEDNum.LED1, 0, 67)
    PCA9685.set_led_duty_cycle(PCA9685.LEDNum.LED2, speed, 67)
    PCA9685.set_led_duty_cycle(PCA9685.LEDNum.LED3, 0, 67)
    PCA9685.set_led_duty_cycle(PCA9685.LEDNum.LED4, speed, 67)
def RobotBack(speed: number):
    PCA9685.set_led_duty_cycle(PCA9685.LEDNum.LED1, 100, 67)
    PCA9685.set_led_duty_cycle(PCA9685.LEDNum.LED2, speed, 67)
    PCA9685.set_led_duty_cycle(PCA9685.LEDNum.LED3, 100, 67)
    PCA9685.set_led_duty_cycle(PCA9685.LEDNum.LED4, speed, 67)
def FrontLedsBlue():
    PCA9685.set_led_duty_cycle(PCA9685.LEDNum.LED5, 0, 67)
    PCA9685.set_led_duty_cycle(PCA9685.LEDNum.LED6, 100, 67)
    PCA9685.set_led_duty_cycle(PCA9685.LEDNum.LED7, 100, 67)
def FrontLedsOff():
    PCA9685.set_led_duty_cycle(PCA9685.LEDNum.LED5, 100, 67)
    PCA9685.set_led_duty_cycle(PCA9685.LEDNum.LED6, 100, 67)
    PCA9685.set_led_duty_cycle(PCA9685.LEDNum.LED7, 100, 67)
def FrontLedsGreen():
    PCA9685.set_led_duty_cycle(PCA9685.LEDNum.LED5, 100, 67)
    PCA9685.set_led_duty_cycle(PCA9685.LEDNum.LED6, 0, 67)
    PCA9685.set_led_duty_cycle(PCA9685.LEDNum.LED7, 100, 67)
def RobotLeft(speed: number):
    PCA9685.set_led_duty_cycle(PCA9685.LEDNum.LED1, 0, 67)
    PCA9685.set_led_duty_cycle(PCA9685.LEDNum.LED2, speed, 67)
    PCA9685.set_led_duty_cycle(PCA9685.LEDNum.LED3, 100, 67)
    PCA9685.set_led_duty_cycle(PCA9685.LEDNum.LED4, speed, 67)
def RobotRight(speed: number):
    PCA9685.set_led_duty_cycle(PCA9685.LEDNum.LED1, 100, 67)
    PCA9685.set_led_duty_cycle(PCA9685.LEDNum.LED2, speed, 67)
    PCA9685.set_led_duty_cycle(PCA9685.LEDNum.LED3, 0, 67)
    PCA9685.set_led_duty_cycle(PCA9685.LEDNum.LED4, speed, 67)
strip = neopixel.create(DigitalPin.P5, 18, NeoPixelMode.RGB)
strip.clear()
PCA9685.reset(67)
RobotStop()

def on_forever():
    strip.show_color(neopixel.colors(NeoPixelColors.RED))
    FrontLedsRed()
    # RobotForward(100)
    basic.pause(500)
    strip.show_color(neopixel.colors(NeoPixelColors.ORANGE))
    basic.pause(500)
    strip.show_color(neopixel.colors(NeoPixelColors.YELLOW))
    basic.pause(500)
    strip.show_color(neopixel.colors(NeoPixelColors.GREEN))
    FrontLedsGreen()
    # RobotBack(100)
    basic.pause(500)
    strip.show_color(neopixel.colors(NeoPixelColors.BLUE))
    basic.pause(500)
    strip.show_color(neopixel.colors(NeoPixelColors.INDIGO))
    FrontLedsBlue()
    # RobotLeft(100)
    basic.pause(500)
    strip.show_color(neopixel.colors(NeoPixelColors.VIOLET))
    basic.pause(500)
    strip.show_color(neopixel.colors(NeoPixelColors.PURPLE))
    # RobotRight(100)
    basic.pause(500)
    strip.show_color(neopixel.colors(NeoPixelColors.WHITE))
    FrontLedsWhite()
    # RobotStop()
    basic.pause(500)
basic.forever(on_forever)
